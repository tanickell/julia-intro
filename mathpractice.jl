# Part 2-1: Vectors and Math

v = [1.0, 2.0, 3.0]
w = [4.0, 5.0, 6.0]

println("v: ", v)
println("w: ", w)


println("\nAddition:")
println(v + w)

println("\nScalar multiplication:")
println(2 * v)

println("\nElement-wise multiplication:")
println(v .* w)

println("\nLength of v: ", length(v))
println("First element of v: ", v[1])


